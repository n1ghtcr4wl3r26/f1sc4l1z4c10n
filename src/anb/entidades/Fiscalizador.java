package anb.entidades;

public class Fiscalizador {
   private String nombre;
   private String codigo;
   private String fechaasig;
   private String usuasig;
   private String id;
   private String cargo;
   private String numero;
   private String ci;

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNombre() {
        return nombre;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setFechaasig(String fechaasig) {
        this.fechaasig = fechaasig;
    }

    public String getFechaasig() {
        return fechaasig;
    }

    public void setUsuasig(String usuasig) {
        this.usuasig = usuasig;
    }

    public String getUsuasig() {
        return usuasig;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    public String getCargo() {
        return cargo;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getNumero() {
        return numero;
    }

    public void setCi(String ci) {
        this.ci = ci;
    }

    public String getCi() {
        return ci;
    }
}
